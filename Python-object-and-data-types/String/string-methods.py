message = "Hello There. My name is Serhat Ece"

message = message.upper()  # Buyuk harf yapar
message = message.lower()  # Kucuk harf yapar
message = message.title()  # Kelımelerın bas harfını buyuk yazar
message = message.capitalize() # Bastakı kelımenın harfnını buyuk yazar
message = message.strip() # Baş ve sondakı karekterleri siler
message = message.split() # Cumleyı karekter dizisine çevirir
message = message.find("Serhat") # Cumlede ıstenen kelıme-harf arar
message = message.startswith("H") # Cumleye bu harf ile mi baslanır ona bakar
message = message.endswith("e") # Cumleye bu harf ile mi sonlanmıs ona bakar
message = message.replace("Ece", "ece") # kelımeyı istenen kelime ile degıstırır
message = message.center(100) # 100 kelımelık bır alan olusturur ve cumleyı ortalar




